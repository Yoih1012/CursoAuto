package javaBasic;

public class Ejemplo_Ciclo_While {

	public static void main(String[] args) {
	 int a = 1;
	 
	 while  (a <= 4) { //Salir del ciclo cuando "a" es mayor que 4
		 System.out.println("El valor de A:" +a);
		 a++; //"incrementar el valor de "a" para la siguiente iteración
	 }

	}

}

